		<h1>10、海螺沟</h1>
		<p>海螺沟冰川森林公园海螺沟距成都319公里，发源于蜀山之王--贡嘎山东面一条冰融河谷，自西南向北注入大渡河支流磨西河。
		沟长30.7公里，面积220平方公里，它聚集了自然冰川，高山雪峰，沸热温泉、原始森林、野生动植物等自然风光。
		海螺沟最让游客流连忘返的莫过于海螺沟的冰川，在海拔较低处就能望见冰川从高峻的峡谷中铺泻而下。
		特别是大冰瀑布，高达1000多米，瑰丽非凡。身处山脚，远望终年积雪不化的贡嘎雪山，气势恢宏，令人肃然起敬。
		海螺沟以其独有的四大特色而有别于我国的所有著名风景旅游区。其一，它是低海拔现代冰川。海螺沟冰川形成于大约1600年前，地质学上称其为现代冰川。
		其二，原始森林与冰川共存。它是贡嘎山最大的一条冰川，长14.2公里，末端落入森林带内6公里，形成冰川与原始森林共生的绝景。
		其三，冰面河、冰面湖、冰下河、冰川城门洞、冰裂隙、冰阶梯、冰石蘑菇、巨大的冰川漂砾、冰川弧拱和极其宽阔的U型冰川峡谷，
		两侧高数百米的留有冰川擦痕的绝壁，和黛绿色的原始森林等，形成惟有海螺沟冰川所有的独特景观。
		四，海螺沟冰川地理条件特殊，除了冬季外，其他季节均可着单衣或夹衣游览冰川。
