		青城山都江堰风景名胜是国家AAAAA旅游景区；2000年被列为世界文化遗产，2006年作为四川大熊猫栖息地的一部分被列入世界自然遗产名录。
		青城山靠岷山雪岭，面向川西平原，群峰环绕，状若城廓；林深树密，四季常绿；丹梯千级，曲径通幽。
