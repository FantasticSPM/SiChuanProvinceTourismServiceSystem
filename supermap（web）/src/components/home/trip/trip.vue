		沟内串珠式的分布着108个大大小小形状各异的海子，虽深过数十米，但可直视海底。
		因水中植物种类和湖底沉积物不同，水色各异，涟漪多彩，晨曦或夕阳下，海中之倒影，更加明净真切，步移景异，
