		<p>九寨沟在四川省南坪县城西45千米处，与甘肃省相接，因有九座藏族村居于其中而得名。
		九寨沟呈人字形，主沟叫树正沟，呈南北延伸，南高北低，向北开口，其上有两条支沟，
